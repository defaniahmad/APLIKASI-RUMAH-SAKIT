    private void penomoran_otomatis() {
        int no_kode_pasien=0;
        String temp_kode_pasien="";
        try {
            koneksi();
            String sql = "select kd_pasien from pasien order by kd_pasien desc limit 1";                        
            ResultSet res=stt.executeQuery(sql);
            while (res.next()) {
                temp_kode_pasien = res.getString(1);                
                String nomor_blkg = temp_kode_pasien.substring(2,4);
                no_kode_pasien=Integer.parseInt(nomor_blkg);
            }
                        
            no_kode_pasien++;
            String nilai=String.valueOf(no_kode_pasien);                      
            if(nilai.length()==1){
                e_idpasien.setText("P00"+no_kode_pasien);
            }else
            if(nilai.length()==2){
                e_idpasien.setText("P0"+no_kode_pasien);
            }else
            if(nilai.length()==3){
                e_idpasien.setText("P"+no_kode_pasien);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Error Penomoran !");
        }
    }
    
